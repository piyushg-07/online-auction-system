import mongoose from "mongoose";

const bidderSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    phone: {
        type: String,
        required: true,
    },
    address: {
        type: String,
        required: true,
    },
    location: {
        type: String,
        required: true,
    },
    city: {
        type: String,
        required: true,
    },
    state: {
        type: String,
        required: true,
    },
    photo: {
        type: String, // Path to the photo
        required: false,
    },
}, { timestamps: true });

const Bidder = mongoose.model("Bidder", bidderSchema);

export default Bidder;